'use strict';

/**
 * @module core/synapse/domain-injector
 * @description Reads activated SYNAPSE domains and writes them as a session file.
 * Concatenates domain file content in layer order (L0→L1→L2→L3→L7).
 */

const fs = require('fs');
const path = require('path');

// Layer sort order — lower index = earlier in output
const LAYER_ORDER = ['L0', 'L1', 'L2', 'L3', 'L7'];

class DomainInjector {
  /**
   * @param {string} synapseDir - Absolute path to .aios-core/core/synapse/
   */
  constructor(synapseDir) {
    this.synapseDir = synapseDir;
    this.domainDir = path.join(synapseDir, '.synapse');
  }

  /**
   * Read activated domain files and concatenate in layer order.
   *
   * @param {Array<{file: string, layer: string}>} activeDomains - From ManifestParser.getActiveDomains()
   * @returns {string} Formatted markdown content
   */
  inject(activeDomains) {
    // Sort domains by layer order
    const sorted = [...activeDomains].sort((a, b) => {
      const ai = LAYER_ORDER.indexOf(a.layer);
      const bi = LAYER_ORDER.indexOf(b.layer);
      // Unknown layers go last
      return (ai === -1 ? 999 : ai) - (bi === -1 ? 999 : bi);
    });

    const sections = [];

    for (const { file, layer } of sorted) {
      const filePath = path.join(this.domainDir, file);

      if (!fs.existsSync(filePath)) {
        console.warn(`[DomainInjector] WARNING: domain file not found, skipping: ${filePath}`);
        continue;
      }

      const content = fs.readFileSync(filePath, 'utf8').trim();
      sections.push(`# ${file} (${layer})\n${content}`);
    }

    const timestamp = new Date().toISOString();
    const header = [
      '# SYNAPSE Active Domains',
      '<!-- Generated by DomainInjector — do not edit manually -->',
      `<!-- Updated: ${timestamp} -->`,
    ].join('\n');

    return [header, ...sections].join('\n\n---\n\n');
  }

  /**
   * Write the injected content to {projectDir}/.aios/active-domains.md.
   *
   * @param {string} content - Result of inject()
   * @param {string} projectDir - Project root directory
   * @returns {string} Absolute path to the written file
   */
  writeSessionFile(content, projectDir) {
    const aiosDir = path.join(projectDir, '.aios');

    if (!fs.existsSync(aiosDir)) {
      fs.mkdirSync(aiosDir, { recursive: true });
    }

    const outputPath = path.join(aiosDir, 'active-domains.md');
    fs.writeFileSync(outputPath, content, 'utf8');
    return outputPath;
  }

  /**
   * Read .aios/active-domains.md and return a status summary.
   *
   * @param {string} projectDir - Project root directory
   * @returns {{ initialized: boolean, activeDomains: string[], updatedAt: string|null, memoryCount: number }}
   */
  getStatus(projectDir) {
    const outputPath = path.join(projectDir, '.aios', 'active-domains.md');

    if (!fs.existsSync(outputPath)) {
      return { initialized: false, activeDomains: [], updatedAt: null, memoryCount: 0 };
    }

    const content = fs.readFileSync(outputPath, 'utf8');

    // Extract timestamp from header comment
    const tsMatch = content.match(/<!-- Updated: ([^-].*?) -->/);
    const updatedAt = tsMatch ? tsMatch[1].trim() : null;

    // Extract active domain names from section headers: "# <file> (<layer>)"
    const domainMatches = [...content.matchAll(/^# (\S+) \(L\w+\)/gm)];
    const activeDomains = domainMatches.map((m) => m[1]);

    // Count memory entries (lines starting with "- " as rough proxy)
    const memoryCount = (content.match(/^- /gm) || []).length;

    return { initialized: true, activeDomains, updatedAt, memoryCount };
  }
}

module.exports = { DomainInjector };
